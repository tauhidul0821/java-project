public class CurrentBalance{
    String No;
    String Taka;

    public CurrentBalance(String No, String Taka) {
        this.No = No;
        this.Taka = Taka;
    }

    public String getNo() {
        return No;
    }

    public void setNo(String No) {
        this.No = No;
    }

    public String getTaka() {
        return Taka;
    }

    public void setTaka(String Taka) {
        this.Taka = Taka;
    }

    

   
    
}